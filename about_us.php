            <p><span class="tab"></span>Welcome to <span id="span1">S&S Diary Private Limited</span>. <br>*S&S Diary Private Limited is 
            located at Maniktola. It was commissioned in 1974 and is a wholly owned subsidiary of the National Dairy Development Board 
            (NDDB). It was an initiative under Operation Flood, the world's biggest dairy development program launched to make India a milk 
            sufficient nation. Over the years, S&S Dairy Pvt Limited has contributed significantly in achieving this objective through a series of 
            innovations and programs. Today, S&S Dairy Pvt Limited manufactures markets & sells milk and milk products including cultured products,
            ice creams, paneer and ghee under the S&S Dairy Pvt Limited brand. The Company also has a diversified portfolio with products in edible oils,
            fruits & vegetables, frozen vegetables, pulses, processed food like fruit juices, jams, etc. to meet the daily requirements of 
            every household. <br> <span class="tab"></span>The Company over the last many years has created a market leadership position for itself in branded milk segment 
            in Delhi & NCR through a robust network of its booth and retail channels. It has also expanded its reach to other regions in North,
            South, East and West with its offering of Milk and Milk products pegging it among the few companies to own such a vast channel of 
            distribution in India. <br>
            <span class="tab"></span>Brand S&S Dairy Management Private Limited sources a significant part of its requirement of liquid milk from dairy cooperatives and village level 
            farmer centric organizations. The Company is committed to uphold institutional structures that empower milk producers 
            and farmers through processes that are equitable. A significant portion of its income is ploughed back into the value chain 
            to support and maintain the system. <br>
            <span class="tab"></span>S&S Dairy Management Private Limited is an ISO 9001:2006 (QMS), ISO 22000:2003 (FSMS) and ISO 14001:2001 (EMS) certified organization. 
            Quality of milk is of paramount importance for the company and hence it has invested extensively in installing hi tech 
            automated machines to ensure high product quality/reliability and safety. <br>
            <span class="tab"></span>Safal, F&V arm of S&S Dairy Management Private Limited was the first Company to organize the fruits and vegetables business in India. Today Safal 
            is the market leader in organized fruit & vegetable retail business in Delhi NCR and operates the largest number of F&V 
            Stores in Delhi NCR and has significant presence in Bangalore. Safal was also the first brand in India to launch frozen 
            vegetable in mid 905. Over the years, the brand has gained significant customer support and has become a household 
            brand with market leadership and presence across the country. <br>
            <span class="tab"></span>Safal also has a state of the art plant in Bangalore which produces and sells around 23000 MT of aseptic fruit pulp & 
            concentrate annually and supplies to noteworthy companies in food processing space like Coca Cola, Pepsi, Unilever, 
            Nestle etc. Safal also has a prominent presence across 40 countries viz., USA, Europe, Russia, Middle East, Asia and Africa 
            and exports Fresh Fruits & Vegetables (Grapes, Banana, Gherkin, Onion, etc.), Fruit Pulp & Concentrate, Frozen Fruits & 
            Vegetables, etc. <br>
            <span class="tab"></span>S&S Dairy Management Private Limited is also present into edible oils segment under the brand name Dhara which was launched under the 'Operation 
