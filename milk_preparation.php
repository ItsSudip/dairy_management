                <div style="margin-top: -30px; margin-left:20px; width: 500px; ">
                <h3>Storage:</h3><span class="tab"></span>Milk storage vats or silos are refrigerated and 
                come in various shapes and sizes. Milk is usually stored on the farm at 39 degrees Fahrenheit, 
                or colder, for no more than 48 hours. Vats and silos are agitated to make sure that the entire 
                volume remains cold and that the milkfat does not separate from the milk. After milk has been 
                collected, storage vats and stainless steel pipes are thoroughly cleaned before the farmer milks again.
                </div>
            </div>
            <div>
            <h3>Lab Testing</h3>
                <p>
                <span class="tab"></span> Samples of milk are taken from farm vats prior to collection and from the bulk milk tanker upon arrival 
                at the factory. Samples from the bulk milk tanker are tested for antibiotics and temperature before the
                milk enters the factory processing area. Farm milk samples are tested for milkfat, protein, bulk milk cell 
                count and bacteria count. If milk does not meet quality standards it is rejected. Most farmers are paid on 
                the quality and composition of their milk.
                </p>
            </div>
            <div>
            <h3>Processing</h3>
                <p>
                <span class="tab"></span> Whole milk, once approved for use, is pumped into storage silos where it undergoes 
                pasteurization, homogenization, separation and further processing.
                <h4>Pasteurization:</h4>
                <span class="tab"></span>Every particle of milk is heated to a specific temperature for a specified period of 
                time and cooling it again without allowing recontamination.
                <h4>Homogenization:</h4>
                <span class="tab"></span>Raw milk is pushed through an atomizer to form tiny particles so that the fat is dispersed evenly throughout 
                the milk, stopping the fat from floating to the top of the container.
                <h4>Separation:</h4>
                <span class="tab"></span>The milk is spun through a centrifuge to separate the cream from the milk. After separation, the cream and 
                remaining milk are remixed to provide the desired fat content for the different types of milk being produced.
                For whole milk the cream is reintroduced until the fat content reaches 3.25%. For low fat milk the fat content 
                is 1%. For skim milk (sometimes called nonfat milk) the fat content is .05%.
                <h4>Further Processing:</h4>
                <span class="tab"></span>This step may involve micro-filtration, increasing the storage life by ultra high temperature (UHT) treatment, 
                and mixing or culturing milk for flavored and yogurt products.
                </p>
            </div>
            <div>
            <h3>Packaging</h3>
                <p>
                <span class="tab"></span> Now the milk is ready to be packaged for delivery to the stores. The milk travels through 
                pipes to the automatic packaging machines that fills and seals the milk into paper cartons or plastic jugs. As the containers
                move through the assembly line, a date is printed on each of them to show how long the milk will stay fresh.
                </p>
